    // Получение списка заявлений пользователя
    getApps(
      page,
      pageSize,
      servId = this.appsServiceId,
      sortCol = "id",
      sortDesc = true,
      userList = true,
      active = false
    ) {
      axios
        .get(
          this.url +
            "app/get-apps?pageNum=" +
            (page - 1) +
            "&pageSize=" +
            pageSize +
            "&servId=" +
            servId +
            "&sortCol=" +
            sortCol +
            "&sortDesc=" +
            sortDesc +
            "&userList=" +
            userList +
            "&active=" +
            active,
          {
            withCredentials: true,
          }
        )
        .then((response) => {
          this.appsTable.rowsList = this.appsConvertToTable(
            response.data.content
          );
          this.appsItemsTotal = response.data.totalElements;
          console.groupCollapsed("Список заявлений");
          console.log(response.data.content);
          console.groupEnd();
        });

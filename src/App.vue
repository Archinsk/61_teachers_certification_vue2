        withCredentials: true,
      }).then((response) => {
        // this.$refs["nav-sidebar"].hide();
        console.log("Ответ на запрос о выходе из ЕСИА");
        console.log(response);
      });
    },
    // Локальный выход
    signOutLocal() {
      axios(this.url + "auth/local-logout", {
